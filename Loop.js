let count = 1;
let word = "Pakistan";

do {
  console.log("Do-while loop count:", word);
  count++;
} while (count <= 3);

let word = "China";

for (let count = 1; count <= 3; count++) {
  console.log(`For loop count ${count}:`, word);
}

let count = 1;
let word = "Pakistan";

while (count <= 3) {
  console.log(`While loop count ${count}:`, word);
  count++;
}

